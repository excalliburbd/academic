DATA SEGMENT

     MSG DB "ERROR!$"

DATA ENDS

CODE SEGMENT

     ASSUME CS: CODE, DS: DATA

START:
  MOV AH,1
  INT 21H

  CMP AL,65
  JL ERROR
  CMP AL,90
  JLE UPPER
  CMP AL,97
  JL ERROR
  CMP AL,122
  JLE LOWER
  JMP FINISH
ERROR:
  MOV AH,9
  LEA DX,MSG
  INT 21H
  JMP FINISH
UPPER:
  ADD AL,32
  MOV DL,AL
  MOV AH,2
  INT 21H
  JMP FINISH
LOWER:
  SUB AL,32
  MOV DL,AL
  MOV AH,2
  INT 21H
  JMP FINISH

FINISH:

CODE ENDS
END START
